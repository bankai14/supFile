class Email extends REST_Controller {

    function __construct()
    {
        // Construct the parent class
        parent::__construct();

        // Configure limits on our controller methods
        // Ensure you have created the 'limits' table and enabled 'limits' within application/config/rest.php
        $this->methods['users_get']['limit'] = 500; // 500 requests per hour per user/key
        $this->methods['users_post']['limit'] = 100; // 100 requests per hour per user/key
        $this->methods['users_delete']['limit'] = 50; // 50 requests per hour per user/key
    }

    public function index_post()
    {
        header("Access-Control-Allow-Origin: *");
        // Envoyer un email
        $this->load->library('email');

        $this->email->from(htmlspecialchars($this->post("email")), htmlspecialchars($this->post("name")));
        $this->email->to('yassine.zitouni@kallam.fr');

        $this->email->subject('Contact kallam.fr');
        $this->email->message(htmlspecialchars($this->post("message")));

        $this->email->send();
        $this->set_response("Mail envoy√©", REST_Controller::HTTP_OK); // CREATED
    }

    private function generateRandomString($length = 10)
    {
        $characters = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';
        $charactersLength = strlen($characters);
        $randomString = '';
        for ($i = 0; $i < $length; $i++) {
            $randomString .= $characters[rand(0, $charactersLength - 1)];
        }
        return $randomString;
    }

}
